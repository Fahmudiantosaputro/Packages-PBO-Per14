    private void btnsubmit2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmit2ActionPerformed
        // TODO add your handling code here: 
        if (txtnim2.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan NIM yang akan diupdate!");
            return;
        }
        if (txtnama2.getText().trim().isEmpty()
                || cbjk2.getSelectedItem() == null
                || txtangkatan2.getText().trim().isEmpty()
                || cbkodeprodi2.getSelectedItem() == null) {

            JOptionPane.showMessageDialog(this, "Isi semua data terlebih dahulu!");
            return;
        }

        String nim = txtnim2.getText().trim();
        String nama = txtnama2.getText().trim();
        String jenisKelamin = cbjk2.getSelectedItem().toString();
        String angkatanStr = txtangkatan2.getText().trim();
        String kodeProdi = cbkodeprodi2.getSelectedItem().toString().split(" - ")[0];

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Apakah Anda yakin ingin memperbarui data dengan NIM: " + nim + "?",
                "Konfirmasi Update",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
        );

        if (confirm != JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, "Update dibatalkan oleh pengguna.");
            return;
        }

        EntityManagerFactory emf = null;
        EntityManager em = null;

        try {
            try {
                Integer.parseInt(angkatanStr);
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(this, "Format angkatan harus angka (contoh: 2023).");
                return;
            }

            emf = Persistence.createEntityManagerFactory("DataMahasiswaPU");
            em = emf.createEntityManager();
            em.getTransaction().begin();

            Mahasiswa m = em.find(Mahasiswa.class, nim);
            if (m == null) {
                JOptionPane.showMessageDialog(this, "Data dengan NIM " + nim + " tidak ditemukan!");
                em.getTransaction().rollback();
                return;
            }

            m.setNama(nama);
            m.setJenisKelamin(jenisKelamin);
            m.setAngkatan(angkatanStr);

            Prodi p = em.find(Prodi.class, kodeProdi);
            if (p == null) {
                JOptionPane.showMessageDialog(this, "Kode Prodi '" + kodeProdi + "' tidak ditemukan di database!");
                em.getTransaction().rollback();
                return;
            }
            m.setProdi(p);

            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");

            tampilData2();
            loadData();

            txtnim2.setText("");
            txtnama2.setText("");
            cbjk2.setSelectedIndex(-1);
            txtangkatan2.setText("");
            cbkodeprodi2.setSelectedIndex(-1);
            txtnim2.setEditable(true);

        } catch (Exception ex) {
            if (em != null && em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Gagal update: " + ex.getMessage());
            ex.printStackTrace();
        } finally {
            if (em != null) {
                em.close();
            }
            if (emf != null) {
                emf.close();
            }
        }
        JDUpdate1.dispose();

    }//GEN-LAST:event_btnsubmit2ActionPerformed

