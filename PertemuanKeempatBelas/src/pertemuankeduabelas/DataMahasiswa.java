    private void btnupload2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupload2ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("DataMahasiswaPU");
            EntityManager em = emf.createEntityManager();

            try (BufferedReader br = new BufferedReader(new FileReader(file))) {
                em.getTransaction().begin();

                String line;
                int count = 0;

                while ((line = br.readLine()) != null) {

                    if (line.trim().isEmpty()) {
                        continue;
                    }

                    String[] data = line.split(";");
                    if (data.length == 3) {

                        try {
                            String kodeProdi = data[0].trim();
                            String namaProdi = data[1].trim();
                            String jenjang = data[2].trim();

                            Prodi p = em.find(Prodi.class, kodeProdi);
                            if (p == null) {
                                p = new Prodi();
                                p.setKodeProdi(kodeProdi);
                            }

                            p.setNamaProdi(namaProdi);
                            p.setJenjang(jenjang);

                            em.persist(p);
                            count++;

                        } catch (Exception e) {
                            System.out.println("Baris dilewati (format salah): " + line);
                        }

                    } else {
                        System.out.println("Format salah: " + line);
                    }
                }

                em.getTransaction().commit();

                JOptionPane.showMessageDialog(this,
                        "Upload berhasil! " + count + " data prodi dimasukkan.",
                        "Sukses", JOptionPane.INFORMATION_MESSAGE);

                loadKodeProdi();
                tampilData2();
                loadData();

            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Kesalahan membaca file: " + e.getMessage());
            } catch (Exception e) {
                if (em.getTransaction().isActive()) {
                    em.getTransaction().rollback();
                }
                JOptionPane.showMessageDialog(this, "Kesalahan upload: " + e.getMessage());
            } finally {
                em.close();
                emf.close();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Upload dibatalkan.", "Informasi", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnupload2ActionPerformed

