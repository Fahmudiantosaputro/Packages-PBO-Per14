    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) tbldata1.getModel();
        model.setRowCount(0);

        Connection conn = null;
        Statement st = null;
        ResultSet rs = null;

        try {
            conn = ReportConnection.getConnection();
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Koneksi database tidak tersedia.");
                return;
            }

            st = conn.createStatement();
            // ambil nama_prodi dari tabel prodi
            String sql = "SELECT m.nim, m.nama, m.jenis_kelamin, m.angkatan, m.kode_prodi, p.nama_prodi "
                    + "FROM mahasiswa m "
                    + "LEFT JOIN prodi p ON m.kode_prodi = p.kode_prodi "
                    + "ORDER BY m.nim";
            rs = st.executeQuery(sql);

            while (rs.next()) {
                String nim = rs.getString("nim");
                String nama = rs.getString("nama");
                String jk = rs.getString("jenis_kelamin");
                // angkatan bisa null sehingga ambil sebagai String agar aman
                Object angkatanObj = rs.getObject("angkatan");
                String angkatan = (angkatanObj == null) ? "" : String.valueOf(angkatanObj);

                // nama prodi (bisa null jika tidak ada matching prodi)
                String namaProdi = rs.getString("nama_prodi");
                if (namaProdi == null || namaProdi.trim().isEmpty()) {
                    // fallback: tampilkan kode prodi jika nama prodi kosong
                    namaProdi = rs.getString("kode_prodi");
                    if (namaProdi == null) {
                        namaProdi = "";
                    }
                }

                model.addRow(new Object[]{nim, nama, jk, angkatan, namaProdi});
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (st != null) {
                    st.close();
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }

    private void loadKodeProdi() {
        EntityManagerFactory emf = null;
        EntityManager em = null;

        try {
            emf = Persistence.createEntityManagerFactory("DataMahasiswaPU");
            em = emf.createEntityManager();

            List<Prodi> list = em.createQuery("SELECT p FROM Prodi p", Prodi.class).getResultList();

            cbkodeprodi1.removeAllItems();

            cbkodeprodi1.addItem("Pilih kode prodi");

            for (Prodi p : list) {
                String item = p.getKodeProdi() + " - " + p.getNamaProdi();

                cbkodeprodi1.addItem(item);
                cbkodeprodi2.addItem(item);
                cbkodeprodi3.addItem(item);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        } finally {
            if (em != null) {
                em.close();
            }
            if (emf != null) {
                emf.close();
            }
        }
    }
