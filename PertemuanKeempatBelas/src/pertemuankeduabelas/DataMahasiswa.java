    private void tampilData1() {
        EntityManagerFactory emf = null;
        EntityManager em = null;
        try {
            emf = Persistence.createEntityManagerFactory("DataMahasiswaPU");
            em = emf.createEntityManager();

            TypedQuery<Mahasiswa> q = em.createQuery("SELECT m FROM Mahasiswa m", Mahasiswa.class);
            List<Mahasiswa> hasil = q.getResultList();

            DefaultTableModel model = new DefaultTableModel(
                    new String[]{"NIM", "Nama", "Jenis Kelamin", "Angkatan", "Nama Prodi"}, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

            for (Mahasiswa m : hasil) {
                Object[] baris = new Object[5];
                baris[0] = m.getNim();
                baris[1] = m.getNama();
                baris[2] = m.getJenisKelamin();
                baris[3] = m.getAngkatan();
                baris[4] = m.getProdi().getNamaProdi();
                model.addRow(baris);
            }

            tbldata1.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan data: " + e.getMessage());
        } finally {
            if (em != null && em.isOpen()) {
                em.close();
            }
            if (emf != null && emf.isOpen()) {
                emf.close();
            }
        }
    }

