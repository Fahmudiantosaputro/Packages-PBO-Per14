    private void btndowload1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndowload1ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Simpan sebagai CSV");

        fileChooser.setSelectedFile(new java.io.File("Mahasiswa.csv"));

        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {

            File fileToSave = fileChooser.getSelectedFile();

            try (FileWriter fw = new FileWriter(fileToSave)) {

                DefaultTableModel model = (DefaultTableModel) tbldata1.getModel();
                int colCount = model.getColumnCount();
                int rowCount = model.getRowCount();

                for (int i = 0; i < colCount; i++) {
                    fw.write(model.getColumnName(i));
                    if (i < colCount - 1) {
                        fw.write(";");
                    }
                }
                fw.write("\n");

                // Tulis data baris
                for (int r = 0; r < rowCount; r++) {
                    for (int c = 0; c < colCount; c++) {
                        Object value = model.getValueAt(r, c);

                        String cell = (value == null) ? "" : value.toString().replace(";", ",");

                        fw.write(cell);

                        if (c < colCount - 1) {
                            fw.write(";");
                        }
                    }
                    fw.write("\n");
                }

                JOptionPane.showMessageDialog(null,
                        "File berhasil disimpan:\n" + fileToSave.getAbsolutePath());

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,
                        "Gagal menyimpan file: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btndowload1ActionPerformed

